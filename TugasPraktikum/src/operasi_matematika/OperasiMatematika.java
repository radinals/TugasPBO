package operasi_matematika;

public class OperasiMatematika {
        public static int pertambahan(int x, int y) {
                return x + y;
        }
        
        public static int pengurangan(int x, int y) {
                return x - y;
        }
        
        public static int perkalian(int x, int y) {
                return x * y;
        } 
        
        public static double pembagian(int x, int y) {
                return (double)x / y;
        }
}
